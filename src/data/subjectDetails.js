  const subject = subjects.find((s) => s.id === subjectId);
  if (subjectId === 'history') return historyDetail;
  if (subjectId === 'computer') return buildComputerDetail(subject);
  if (subjectId === 'economics') return buildEconomicsDetail(subject);
