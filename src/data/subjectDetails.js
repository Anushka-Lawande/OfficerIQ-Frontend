function genericDetail(subject) {
  const perChapter = Math.max(2, Math.round(subject.topics / 3));
  const makeTopics = (n) =>
    Array.from({ length: Math.max(n, 1) }, (_, i) => ({
      title: `Topic ${i + 1}`,
      duration: '10m',
      type: 'Video',
    }));
  const last = Math.max(subject.topics - perChapter * 2, 1);
  return {
    tagline: subject.subtitle,
    overallPercent: subject.percent,
    completedCount: Math.round((subject.percent / 100) * subject.topics),
    totalCount: subject.topics,
    chapters: [
      { title: 'Chapter 1', topicsCount: perChapter, hasTest: true, topics: makeTopics(perChapter) },
      { title: 'Chapter 2', topicsCount: perChapter, hasTest: true, topics: makeTopics(perChapter) },
      { title: 'Chapter 3', topicsCount: last, hasTest: true, topics: makeTopics(last) },
    ],
    quote: null,
  };
}

